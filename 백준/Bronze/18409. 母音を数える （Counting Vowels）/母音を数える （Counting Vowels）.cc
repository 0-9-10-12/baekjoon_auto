void Solution()
{
    int n;
    std::string s;
   std::cin >> n >> s;

    int ans{};
    for (int i{}; i < n; ++i) {
        if (s[i] == 'a' || s[i] == 'e' || s[i] == 'i' || s[i] == 'o' || s[i] == 'u') {
         ++ans;
      }
    }

   std::cout << ans;
}