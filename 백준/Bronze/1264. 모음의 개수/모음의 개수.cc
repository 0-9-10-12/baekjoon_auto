void Solution()
{
    while (true) {
        //std::cin.ignore();
        std::string s;
        std::getline(std::cin, s);

        if (s == "#") {
            break;
        }

        int n{};
        for (int i{}; i < s.size(); ++i) {
            if (s[i] == 'a' || s[i] == 'e' || s[i] == 'i' || s[i] == 'o' || s[i] == 'u'
                || s[i] == 'A' || s[i] == 'E' || s[i] == 'I' || s[i] == 'O' || s[i] == 'U') {
                ++n;
			}
        }

		std::cout << n << '\n';
    }
}